package function;

public class fobat {
   private String kd_obat, nama_obat, id_jenis, keterangan, dosis;
   private int stok, harga; 
   
   public String getKd_obat(){ return kd_obat; }
   public void setKd_obat(String kd_obat){ this.kd_obat=kd_obat; }
   public String getNama_obat(){ return nama_obat; }
   public void setNama_obat(String nama_obat){ this.nama_obat=nama_obat; }
   public String getId_jenis(){ return id_jenis; }
   public void setId_jenis(String id_jenis){ this.id_jenis=id_jenis; }
   public String getKeterangan(){ return keterangan; }
   public void setKeterangan(String keterangan){ this.keterangan=keterangan; }
   public String getDosis(){ return dosis; }
   public void setDosis(String dosis){ this.dosis=dosis; }
   public int getStok(){ return stok; }
   public void setStok(int stok){ this.stok=stok; }
   public int getHarga(){ return harga; }
   public void setHarga(int harga){ this.harga=harga; }
}
